package com.se.simplicity.editor.internal;

/**
 * <p>
 * The widgets that can be used manipulate the content of a {@link com.se.simplicity.editor.internal.ContentProvider ContentProvider}.
 * </p>
 * 
 * @author Gary Buyn
 */
public enum Widget
{
    /**
     * <p>
     * No widget.
     * </p>
     */
    NONE,

    /**
     * <p>
     * The rotation widget.
     * </p>
     */
    ROTATION,

    /**
     * <p>
     * The translation widget.
     * </p>
     */
    TRANSLATION
}
