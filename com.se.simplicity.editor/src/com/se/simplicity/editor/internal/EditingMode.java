package com.se.simplicity.editor.internal;

/**
 * <p>
 * An editing mode used for manipulating the content of a {@link com.se.simplicity.editor.internal.ContentProvider ContentProvider}.
 * </p>
 * 
 * @author Gary Buyn
 */
public enum EditingMode
{
    /**
     * <p>
     * Rotation mode.
     * </p>
     */
    ROTATION,

    /**
     * <p>
     * Selection mode.
     * </p>
     */
    SELECTION,

    /**
     * <p>
     * Translation mode.
     * </p>
     */
    TRANSLATION
}
